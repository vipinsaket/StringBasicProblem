package Strings;

public class length_and_charAt {
    public static void main(String[] args) {
        String s="JSpiders";
        System.out.println(s.length());
        System.out.println(s.charAt(3));
        System.out.println(s.charAt(s.length()));
        System.out.println(s.charAt(s.length()-1));

    }
}
